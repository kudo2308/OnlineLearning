package model;

import java.math.BigDecimal;
import java.util.Date;

public class Transaction {
    private int transactionId;
    private Integer orderId;  // can be null
    private Integer expertId; // can be null
    private BigDecimal amount;
    private String transactionType; // allowed: "payment", "payout", "commission", "refund"
    private String description;
    private String status;          // defaults to "pending"; allowed: "pending", "completed", "failed"
    private Date createdAt;
    private User user; // Added for dashboard display

    public Transaction() {
    }

    // Getters and Setters
    public int getTransactionId() {
        return transactionId;
    }
    public void setTransactionId(int transactionId) {
        this.transactionId = transactionId;
    }
    public Integer getOrderId() {
        return orderId;
    }
    public void setOrderId(Integer orderId) {
        this.orderId = orderId;
    }
    public Integer getExpertId() {
        return expertId;
    }
    public void setExpertId(Integer expertId) {
        this.expertId = expertId;
    }
    public BigDecimal getAmount() {
        return amount;
    }
    public void setAmount(BigDecimal amount) {
        this.amount = amount;
    }
    public String getTransactionType() {
        return transactionType;
    }
    public void setTransactionType(String transactionType) {
        this.transactionType = transactionType;
    }
    public String getDescription() {
        return description;
    }
    public void setDescription(String description) {
        this.description = description;
    }
    public String getStatus() {
        return status;
    }
    public void setStatus(String status) {
        this.status = status;
    }
    public Date getCreatedAt() {
        return createdAt;
    }
    public void setCreatedAt(Date createdAt) {
        this.createdAt = createdAt;
    }
    public User getUser() {
        return user;
    }
    public void setUser(User user) {
        this.user = user;
    }
}
